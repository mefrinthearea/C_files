#include <stdio.h>

int main() {

  /* Escape sequence = character combination consisting of backslash \ followed by a letter or combination of digits. 
  They specify actions within a line or a string of text.
  \n = new lines
  \t = new tab
  */

  printf("I\nlike\npizza!\n");
  
  // calculator grid
  printf("1\t2\t3\n4\t5\t6\n7\t8\t9\n\t0\n");
  printf("\"I like Pizza\" - Abraham Lincoln\n");
  return 0;
}
